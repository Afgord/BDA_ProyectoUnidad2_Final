    private void cargarDatosPerfil() {
        try {
            Estudiante actualizado = estudianteService.buscarPorId(usuarioActual.getId());
            this.usuarioActual = actualizado;

            lblNombre.setText(actualizado.getNombre());
            lblApPat.setText(actualizado.getApPat());
            lblApMat.setText(actualizado.getApMat());
            lblCorreo.setText(actualizado.getCorreoInst());

            txtCarrera.setText(actualizado.getCarrera() != null ? actualizado.getCarrera() : "");
            txtFoto.setText(actualizado.getFotoPerfil() != null ? actualizado.getFotoPerfil() : "");
            txtDescripcion.setText(actualizado.getDescripcion() != null ? actualizado.getDescripcion() : "");

            lblTitulo.setText("PERFIL DE " + actualizado.getNombre());

            cargarHobbies();
            cargarComboHobbies();

        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void cargarHobbies() {
        try {
            Estudiante e = estudianteService.buscarPorIdConHobbies(usuarioActual.getId());
            this.usuarioActual = e;

            DefaultTableModel modelo = (DefaultTableModel) tblHobbies.getModel();
            modelo.setRowCount(0);

            for (Hobby h : e.getHobbies()) {
                modelo.addRow(new Object[]{
                    h.getId(),
                    h.getNombre()
                });
            }

        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar hobbies");
        }
    }

    private void cargarComboHobbies() {
        try {
            cmbHobbies.removeAllItems();

            List<Hobby> hobbies = hobbyService.obtenerTodos();

            Estudiante e = estudianteService.buscarPorIdConHobbies(usuarioActual.getId());
            this.usuarioActual = e;

            Set<Hobby> hobbiesUsuario = e.getHobbies();

            for (Hobby h : hobbies) {
                boolean yaLoTiene = hobbiesUsuario.stream()
                        .anyMatch(hu -> hu.getId().equals(h.getId()));

                if (!yaLoTiene) {
                    cmbHobbies.addItem(h.getNombre());
                }
            }

        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
