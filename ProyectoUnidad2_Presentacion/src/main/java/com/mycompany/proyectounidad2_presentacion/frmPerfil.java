            String nombre = JOptionPane.showInputDialog(this, "Nombre del hobby:");

            if (nombre == null || nombre.isBlank()) {
                return;
            }

            Hobby nuevo = new Hobby(nombre.trim(), "");
            nuevo = hobbyService.registrarHobby(nuevo);

            estudianteService.agregarHobby(usuarioActual.getId(), nuevo.getId());

